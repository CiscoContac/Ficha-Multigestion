public class beanCampania {
    private String nomCampa;
    private String descrBoni;
    private String fechIni;
    private String fechFin;

    /**
     * @return the nomCampa
     */
    public String getNomCampa() {
        return nomCampa;
    }

    /**
     * @param nomCampa the nomCampa to set
     */
    public void setNomCampa(String nomCampa) {
        this.nomCampa = nomCampa;
    }

    /**
     * @return the descrBoni
     */
    public String getDescrBoni() {
        return descrBoni;
    }

    /**
     * @param descrBoni the descrBoni to set
     */
    public void setDescrBoni(String descrBoni) {
        this.descrBoni = descrBoni;
    }

    /**
     * @return the fechIni
     */
    public String getFechIni() {
        return fechIni;
    }

    /**
     * @param fechIni the fechIni to set
     */
    public void setFechIni(String fechIni) {
        this.fechIni = fechIni;
    }

    /**
     * @return the fechFin
     */
    public String getFechFin() {
        return fechFin;
    }

    /**
     * @param fechFin the fechFin to set
     */
    public void setFechFin(String fechFin) {
        this.fechFin = fechFin;
    }
    
}
